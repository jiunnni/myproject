package oo.abstraction;

public class Fruit {
	String name;
	String shape;
	int price;
	String color;
	int status;
	int id;
	public Fruit (String name, String shape,int price,String color){
		this.name=name;
		this.shape=shape;
		this.price=price;
		this.color=color;
		this.status=status;
		this.id=id;	
	}
}
