package oo.mid;

public class Ticket {
	int id;
String name;
String price;
public void setId(int id){
	this.id=id;
}
public int getId(){
	return id;
}
	public void setName(String name){
	this.name=name;
	}
	public String getName(){
		return name;
	}
	public void setPrice(String price){
	this.price=price;
	}
	public String getPrice(){
		return price;
}
}
